class Contact:
    def __init__(self, firstname=None, middlename=None, lastname=None, nickname=None, company=None, title=None, address=None, mobile=None):
        self.firstname = firstname
        self.middlename = middlename
        self.lastname = lastname
        self.nickname = nickname
        self.address = address
        self.mobile = mobile
        self.company = company
        self.title = title
